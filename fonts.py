FG_WHITE = "\033[37m" 
FG_CYAN = "\033[36m"
FG_MAGENTA = "\033[35m"
BG_YELLOW = "\033[43m"
BG_CYAN = "\033[46m"
BG_GREEN = "\033[42m"
BOLD = "\033[1m"
UNDERLINE = "\033[4m"
ANSI_RESET  = "\033[0m"